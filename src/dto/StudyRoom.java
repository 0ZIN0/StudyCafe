package dto;

public class StudyRoom {
	
	private String studyRoom_id;
	private String studyRoom_category;
	private String studyRoom_state;
	
	public String getStudyroom_id() {
		return studyRoom_id;
	}
	public void setStudyroom_id(String studyroom_id) {
		this.studyRoom_id = studyroom_id;
	}
	public String getStudyroom_category() {
		return studyRoom_category;
	}
	public void setStudyroom_category(String studyroom_category) {
		this.studyRoom_category = studyroom_category;
	}
	public String getStudyroom_state() {
		return studyRoom_state;
	}
	public void setStudyroom_state(String studyroom_state) {
		this.studyRoom_state = studyroom_state;
	}
}
